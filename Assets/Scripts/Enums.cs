    private static readonly Random random = new Random();

    public static string GetRandomStopCode(bool win7 = false)
    {
        if (win7)
            return Win7StopCodes[random.Next(Win7StopCodes.Length)];
        else
            return Win10StopCodes[random.Next(Win10StopCodes.Length)];
